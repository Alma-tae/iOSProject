//swift나 Kotlin, C#, javascript는 기존 클래스나 객체에 기능 추가 가능 => 별도의 클래스 생성할 필요 없음
extension Int16{
    func toLogType() -> String{
        switch self {
        case 0 : return "생성"
        case 1 : return "수정"
        case 2 : return "삭제"
        default : return ""
        }
    }
}

